        this.form.user == this.save.user &&
        this.form.password == this.save.password
      ) {
        this.$store.state.changeLu = 1;
        this.$router.push({
          path: "/home",
        });
      } else {
        if (this.checked != true) {
          const h = this.$createElement;

          this.$message({
            message: h("p", null, [
              h("span", null, "温馨提示 "),

              h("i", { style: "color: teal" }, "请同意协议"),
            ]),
          });
        } else if (
